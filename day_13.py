def find_mirror(grid):
    for i in range(0, len(grid) - 1):
        match = True
        bf = min(i+1, len(grid) - i-1)
        #print(f"i={i},bf={bf}")
        for j in range(0,bf):
            print(f"check {i-j}=={i+j+1}")
            if grid[i-j] != grid[i+j+1]:
                match = False
        if match:
            #print("match found!!!:", i)
            return i+1
    return None
